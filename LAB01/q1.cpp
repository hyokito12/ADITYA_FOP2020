#include<iostream>
using namespace std;

int main(){
  // Variable declaration
  int num1 = 12, num2 = 5;

  // Display output
  cout  << num1 << " + " << num2 << " = "
        << num1 + num2 << endl

        << num1 << " - " << num2 << " = "
        << num1 - num2 << endl

        << num1 << " x " << num2 << " = "
        << num1 * num2 << endl

        << num1 << " : " << num2 << " = "
        << num1 / num2 << endl
        
        << num1 << " % " << num2 << " = "
        << num1 % num2 << endl;
  
}